_INDEX = {g[1]: g for g in GUA_64}


def get_gua(name: str) -> dict | None:
    """按卦名查询"""
    g = _INDEX.get(name)
    if not g:
        return None
    num, name, upper, lower, judgement, advice = g
    return {
        "number": num,
        "name": name,
        "upper": upper,
        "lower": lower,
        "judgement": judgement,
        "naming_advice": advice,
    }


def get_gua_by_hour(hour: int) -> str:
    """
    根据出生时辰（24 小时制）粗略起卦。
    简化映射：用时辰对应离/坎/坤/乾 等本卦做提示
    （正式起卦需结合年月日时干支，这里只做参考）
    """
    # 子时(23-1)坎、丑寅(1-5)艮、卯(5-7)震、辰巳(7-11)巽
    # 午(11-13)离、未申(13-17)坤、酉(17-19)兑、戌亥(19-23)乾
    if hour >= 23 or hour < 1: return "坎"
    if 1 <= hour < 5: return "艮"
    if 5 <= hour < 7: return "震"
    if 7 <= hour < 11: return "巽"
    if 11 <= hour < 13: return "离"
    if 13 <= hour < 17: return "坤"
    if 17 <= hour < 19: return "兑"
    return "乾"


