# 索引：(日主, 月支) → 规则
_INDEX = {(r[0], r[1]): r for r in TIAOHOU_RULES}


def get_tiaohou(day_master: str, birth_month: str) -> dict:
    """
    查询调候用神

    Args:
        day_master: 日主天干，如 "甲"
        birth_month: 月支地支，如 "丑"

    Returns:
        {
            "day_master": ...,
            "birth_month": ...,
            "month_name": "十二月",
            "primary_yongshen": "丁",
            "secondary_yongshen": "庚",
            "primary_wuxing": "火",
            "secondary_wuxing": "金",
            "avoid_wuxing": "水",
            "explanation": "..."
        }
    """
    rule = _INDEX.get((day_master, birth_month))
    if not rule:
        return {
            "day_master": day_master,
            "birth_month": birth_month,
            "month_name": DIZHI_MONTH.get(birth_month, "?"),
            "primary_yongshen": None,
            "secondary_yongshen": None,
            "primary_wuxing": None,
            "secondary_wuxing": None,
            "avoid_wuxing": None,
            "explanation": f"未找到规则：{day_master}日{birth_month}月",
        }

    _, _, p_ys, s_ys, p_wx, s_wx, avoid, expl = rule
    return {
        "day_master": day_master,
        "birth_month": birth_month,
        "month_name": DIZHI_MONTH.get(birth_month, "?"),
        "primary_yongshen": p_ys,
        "secondary_yongshen": s_ys,
        "primary_wuxing": p_wx,
        "secondary_wuxing": s_wx,
        "avoid_wuxing": avoid or None,
        "explanation": expl,
    }


