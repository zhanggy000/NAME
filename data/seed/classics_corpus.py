def get_classics_for_char(ch: str) -> list[dict]:
    """查询含某字的所有典籍引用"""
    results = []
    for book, chapter, line, era, author in CLASSICS_CORPUS:
        if ch in line:
            results.append({
                "book": book,
                "chapter": chapter,
                "line": line,
                "era": era,
                "author": author,
            })
    return results


def get_books_summary() -> dict:
    """统计各典籍的录入条数"""
    counts = {}
    for entry in CLASSICS_CORPUS:
        book = entry[0]
        counts[book] = counts.get(book, 0) + 1
    return counts


