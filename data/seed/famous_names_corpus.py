# 字 → 名人 索引
_CHAR_INDEX: dict[str, list[dict]] = {}
for entry in FAMOUS_NAMES:
    full, surname, given, cat, era, gender, brief, fame = entry
    for ch in given:
        record = {
            "full_name": full,
            "surname": surname,
            "given_name": given,
            "category": cat,
            "era": era,
            "gender": gender,
            "brief": brief,
            "fame_score": fame,
        }
        _CHAR_INDEX.setdefault(ch, []).append(record)


def get_famous_for_char(ch: str, limit: int = 10) -> list[dict]:
    """查询用过某字的名人（按知名度倒序）"""
    res = _CHAR_INDEX.get(ch, [])
    res = sorted(res, key=lambda r: r["fame_score"], reverse=True)
    return res[:limit]


